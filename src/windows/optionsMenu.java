    private void BTretirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTretirarActionPerformed
        try {
            String val = JOptionPane.showInputDialog("Escribe cuanto dinero desea retirar");
            if (val != null && Double.parseDouble(val) < caja.getCantidad() && Double.parseDouble(val) > 0) {
                Double r = -Double.parseDouble(val);
                caja.setCantidad(r);
                SQLquerys.retiro(r, "retiro");
                LBtotalCaja.setText("$"+(caja.getCantidad()+caja.getDinInicial()));
                SQLquerys.guardarcaja();
            }else if (Double.parseDouble(val) > caja.getCantidad()) {
                JOptionPane.showMessageDialog(null, "No hay suficiente dinero en la caja");
            }else if (Double.parseDouble(val) < 0){
                JOptionPane.showMessageDialog(null, "Escriba un valor positivo, por favor");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Escriba un numero por favor");
            System.err.println("Error: "+e);
        }
    }//GEN-LAST:event_BTretirarActionPerformed

