    private void BTagregarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTagregarUsuarioActionPerformed
        try{
            if (TFagregarNombreDeUsuario.getText().isBlank()|| PFagregarPassUsuario.getText().isBlank() || PFagregarPass2Usuario.getText().isBlank() || CBagregarTipoUsuario.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Agregue los datos necesarios, por favor");
            }else if(CBagregarPersona.getSelectedIndex() == 0){
                int op = JOptionPane.showOptionDialog(null, "¿Desea agregar a una nueva persona?", "Nueva persona", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, new String[]{"Sí", "No"},"No");
                if (op == JOptionPane.YES_OPTION) {
                    ap = new agregarPersona();
                    ap.setVisible(true);
                    ap.setOm(this); 
                    setEnabled(false);
                }
            }else{
                if (PFagregarPassUsuario.getText().equals(PFagregarPass2Usuario.getText()) && SQLquerys.canUseNombre("Usuario", TFagregarNombreDeUsuario.getText())) {
                    SQLquerys.agregarUsu(PFagregarPassUsuario.getText(), TFagregarNombreDeUsuario.getText(), CBagregarTipoUsuario.getSelectedIndex(), CBagregarPersona.getSelectedItem().toString().substring(0, 6));
                    SQLquerys.llenarTablaUsuarios((DefaultTableModel) TBusuarios.getModel());
                }else{
                    if (!PFagregarPassUsuario.getText().equals(PFagregarPass2Usuario.getText())) {
                        JOptionPane.showMessageDialog(null, "Las contraseñas deben de coincidir");
                    }else{
                        JOptionPane.showMessageDialog(null, "Ese nombre de usuario ya esta en uso");
                    }
                }
            }
        }catch(Exception e){
            System.err.println("Error en el boton: "+e);
        }
        repaint();
    }//GEN-LAST:event_BTagregarUsuarioActionPerformed

